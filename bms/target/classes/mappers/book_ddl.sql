USE bms;
CREATE TABLE BOOK(
    BOOK_CD 		  INT AUTO_INCREMENT PRIMARY KEY,
    BOOK_NM 		  VARCHAR(500),
    WRITER 			  VARCHAR(200),
	PRICE 			  INT,
	DISCOUNT_RT 	  INT,
	STOCK			  INT,
	PUBLISHER 		  VARCHAR(50),
	SORT 			  VARCHAR(10),
	POINT 			  INT,
	PUBLISHED_DT 	  TIMESTAMP,
	TOTAL_PAGE 		  INT,
	ISBN 			  VARCHAR(50),
	DELIVERY_PRICE 	  INT,
	PART 			  VARCHAR(20),
	WRITER_INTRO 	  VARCHAR(2000),
	CONTENTS_ORDER 	  VARCHAR(2000),
	INTRO 			  VARCHAR(2000),
	PUBLISHER_COMMENT VARCHAR(2000),
	RECOMMENDATION 	  VARCHAR(2000),
	IMG_NM	  		  VARCHAR(300),
	ENROLL_DT 		  TIMESTAMP
);